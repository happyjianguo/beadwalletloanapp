package com.waterelephant.bairong.entity;

public class ResponseResult {
	
	private String id;
	
	private String cell;
	
	private String name;
	
	private String mail;
	
	private String event;
	
	private String af_swift_number;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCell() {
		return cell;
	}

	public void setCell(String cell) {
		this.cell = cell;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

	public String getEvent() {
		return event;
	}

	public void setEvent(String event) {
		this.event = event;
	}

	public String getAf_swift_number() {
		return af_swift_number;
	}

	public void setAf_swift_number(String af_swift_number) {
		this.af_swift_number = af_swift_number;
	}
	
	

}
